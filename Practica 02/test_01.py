"""
    Tipos de datos trascendentales y complementarios
"""

"""Tipo de datos: Lista []"""

var_1 = [20, "Luis", "Calle Las Almendras 145", 2000]

print("El valor de mi variable 'var_1' es: {}".format(var_1))

"""Tipo de datos: Tuplas ()"""

my_tupla = (40, 80, 170, 2000, "Hola Pythonista")

print("El valor de mi variable 'my_tupla' es: {}".format(my_tupla))

"""Tipo de datos: Set ()"""

my_set = set(["Bienvenidos", 50, 10, 40.77, False, 1000, 50, 50, 10])
print("El valor de mi variable 'my_set' es: {}".format(my_set))

my_dict = {"nombre": "Lucia", "edad": 27, "dni": 87654321}

print("El valor de mi variable 'my_dict' es: {}".format(my_dict))
