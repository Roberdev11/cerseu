"""Listas en Python"""

"""Listas (copy): Copia todos los valores de una lista a otra variable"""

lista_1 = ["Susana", "Dominguez", 39, "20/04/1984", "99998888"]

lista_2 = lista_1.copy()
lista_2.append("Jesús María.")

print("El contenido de mi lista_1 es: {}".format(lista_1))
print("El contenido de mi lista_2 es: {}".format(lista_2))
