"""Listas en Python"""

"""Listas del: Eliminar un valor de la lista enviando una posición existente"""

lista_1 = [0, 2, 4, 6, 16, 20, 26]

del lista_1[2]

print("Mi lista actualizada tiene los siguiente valores: {}".format(lista_1))

"""Eliminando el último valor de la lista"""
del lista_1[-1]
print("Mi lista actualizada tiene los siguiente valores: {}".format(lista_1))