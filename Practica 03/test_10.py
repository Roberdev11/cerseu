"""Listas en Python"""

"""Listas (reverse): Revierte el orden de los elementos de una lista"""

datos_empleado = ["Susana", "Dominguez", 39, "20/04/1984", "99998888"]

print("Los datos de mi lista inicial es: {}".format(datos_empleado))

datos_empleado.reverse()
print("Los valores de mi nueva lista son: {}".format(datos_empleado))
print(datos_empleado)
